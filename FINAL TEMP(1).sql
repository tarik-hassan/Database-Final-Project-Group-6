SELECT s.first_name, s.last_name, t.studentID, t.courseID, t.final_grade, lg.letter
FROM (
    SELECT sg.studentID, sg.courseID,
        SUM(sg.grade *a.weight / 100) AS final_grade
    FROM STUDENT_GRADES sg
    JOIN ASSESSMENT a ON sg.assessmentID = a.assessmentID
    GROUP BY sg.studentID, sg.courseID
) t
JOIN STUDENTS s ON t.studentID = s.studentID
JOIN LETTER_GRADE lg
ON t.final_grade BETWEEN lg.min_score AND lg.max_score;

